package day3;
//1.Java Program to print the elements of an array
public class PrintArray {
    public static void main(String[] args) {
        // Create an array
        int [] numbers ={9,8,7,6,5,4,3,2,1};

        for (int i=0; i<numbers.length; i++){
            System.out.println(numbers[i]);
        }
    }
}
